#!/usr/bin/env node
/**
 * Generates _redirects for Cloudflare Pages.
 * Enables clean URLs (no .html) by proxying to .html files with 200 status.
 * Run: node scripts/generate-redirects.js
 */
const fs = require('fs');
const path = require('path');

const OUTPUT = path.join(__dirname, '..', '_redirects');

function walk(dir, files = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = path.join(dir, e.name);
    if (e.isDirectory() && !['node_modules', 'scripts', 'templates', 'assets', '.git'].includes(e.name)) {
      walk(full, files);
    } else if (e.isFile() && e.name.endsWith('.html')) {
      const rel = path.relative(path.join(__dirname, '..'), full).replace(/\\/g, '/');
      files.push(rel);
    }
  }
  return files;
}

const files = walk(path.join(__dirname, '..'));
const lines = [
  '# Cloudflare: 301 .html -> clean URL only. Rewrites handled by Workers html_handling.',
  '# Generated by scripts/generate-redirects.js',
  '',
  '# 301: .html URLs redirect to clean canonical URLs',
  '# (200 rewrites removed - caused infinite loop with /index.html; Workers html_handling serves clean URLs)',
  '/index.html / 301',
  '/blog/index.html /blog 301',
  '/blog/:slug.html /blog/:slug 301',
  '/:path.html /:path 301',
];

fs.writeFileSync(OUTPUT, lines.join('\n'), 'utf8');
console.log(`Generated _redirects (${files.length} HTML files mapped via placeholders)`);
